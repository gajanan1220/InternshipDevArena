# 1. Creating Strings
name = "Gajanan"
city = 'Mumbai'

# 2. Combining Strings (Concatenation)
first = "Data"
second = "Science"
combined = first + " " + second
print("Combined:", combined)

# 3. Repeating Strings
word = "Hi "
print("Repeated:", word * 3)

# 4. Accessing Characters
text = "Python"
print("First character:", text[0])
print("Fourth character:", text[3])

# 5. Finding Length
print("Length:", len(text))

# 6. Converting Case
msg = "hello world"
print("Uppercase:", msg.upper())
print("Lowercase:", msg.lower())

# 7. Checking if text contains a word
print("Contains 'Python'?", "Python" in "I love Python")
